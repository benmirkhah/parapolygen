//-----------------------------------------------------------------------------
export function isNull  (x) { return x === null; }
//-----------------------------------------------------------------------------
export function isSet   (x) { return typeof x !== 'undefined'; }
//-----------------------------------------------------------------------------
export function notSet  (x) { return typeof x === 'undefined'; }
//-----------------------------------------------------------------------------
export function isBool  (x) { return typeof x === 'boolean'; }
//-----------------------------------------------------------------------------
export function isStr   (x) { return typeof x === 'string'; }
//-----------------------------------------------------------------------------
export function isNum   (x) { return typeof x === 'number'; }
//-----------------------------------------------------------------------------
export function isArray (x) { return Array.isArray(x); }
//-----------------------------------------------------------------------------
export function isInt   (x) { return isNumber(x) && x === Math.floor(x); }
//-----------------------------------------------------------------------------
export function isObj   (x) { return !isNull(x) && !isArray(x) && typeof x === 'object'; }
//-----------------------------------------------------------------------------
